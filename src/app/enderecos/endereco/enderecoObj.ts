export function EnderecoObj(rua: string, numero: number, complemento: string, bairro: string, cidade: string, estado: string, cep: string){

    var rua: string;
    var numero: number;
    var complemento: string;
    var bairro: string;
    var cidade: string;
    var estado: string;
    var cep: string;

    this.rua = rua;
    this.numero = numero;
    this.complemento = complemento;
    this.bairro = bairro;
    this.cidade = cidade;
    this.estado = estado;
    this.cep = cep;
    
}